    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (txtTeacherId.getText().isEmpty()
            || txtTeacherName.getText().isEmpty()
            || txtPhoneNumber.getText().isEmpty()
            || txtEmail.getText().isEmpty()
            || txtSpecialization.getText().isEmpty()) {
              JOptionPane.showMessageDialog(this, "Please fill in all fields.");
              return; }
            
            int teacherId = Integer.parseInt(txtTeacherId.getText());//because it receive it as string
            String teacherName = txtTeacherName.getText();
            String phoneNumber = txtPhoneNumber.getText();
            String email = txtEmail.getText();
            String department = departmentCombo.getSelectedItem().toString();
            String specialization = txtSpecialization.getText();
            TeacherStatus status = TeacherStatus.valueOf(
            statusCombo.getSelectedItem().toString());
            
            if (phoneNumber.length() != 8) {
              JOptionPane.showMessageDialog(this,"Phone number must contain 8 digits.");
              return; }

            if (!email.contains("@") || !email.contains(".")) {
              JOptionPane.showMessageDialog(this, "Invalid email.");
              return; }

            Teacher teacher = new Teacher(teacherId, teacherName, phoneNumber, email, department,
            specialization, status );

    if (manager.addTeacher(teacher)) {
        JOptionPane.showMessageDialog(this,"Teacher added successfully.");
        refreshTable();
    } 
    else {
        JOptionPane.showMessageDialog(this, "Teacher ID already exists.");
    }

} catch (NumberFormatException e) {

    JOptionPane.showMessageDialog(this,"Teacher ID must be a number.");

}
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            int teacherId = Integer.parseInt(txtTeacherId.getText());
            String teacherName = txtTeacherName.getText();
            String phoneNumber = txtPhoneNumber.getText();
            String email = txtEmail.getText();
            String department = departmentCombo.getSelectedItem().toString();
            String specialization = txtSpecialization.getText();

            TeacherStatus status = TeacherStatus.valueOf(
            statusCombo.getSelectedItem().toString());

            boolean updated = manager.updateTeacher(teacherId, teacherName, phoneNumber, email,
            department, specialization, status);

            if (updated) {
             JOptionPane.showMessageDialog(this,"Teacher updated successfully." );
             refreshTable();
            } 
            else {
             JOptionPane.showMessageDialog(this,"Teacher ID not found.");
            }

        }
        catch (NumberFormatException e) {
           JOptionPane.showMessageDialog(this,"Teacher ID must be a number." );
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

