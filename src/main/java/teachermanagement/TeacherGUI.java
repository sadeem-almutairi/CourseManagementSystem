    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    try {
    String searchType = jComboBox1.getSelectedItem().toString();
    String keyword = jTextField1.getText();

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    model.setRowCount(0);
    
    if (searchType.equals("Teacher ID")) { // searching by ID
        int teacherId = Integer.parseInt(keyword);
        Teacher teacher = manager.searchTeacherById(teacherId);

        if (teacher != null) {
            model.addRow(new Object[]{
                teacher.getTeacherId(),
                teacher.getTeacherName(),
                teacher.getDepartment(),
                teacher.getStatus()
            });
        } else {
            JOptionPane.showMessageDialog(this,"Teacher not found.");
        }

    } 
    else if (searchType.equals("Teacher Name")) {//searching by name
        for (Teacher teacher : manager.searchTeacherByName(keyword)) {
            model.addRow(new Object[]{
                teacher.getTeacherId(),
                teacher.getTeacherName(),
                teacher.getDepartment(),
                teacher.getStatus()
            });
        }

    } 
    else if (searchType.equals("Department")) {//searching by department
        for (Teacher teacher : manager.searchTeacherByDepartment(keyword)) {
            model.addRow(new Object[]{
                teacher.getTeacherId(),
                teacher.getTeacherName(),
                teacher.getDepartment(),
                teacher.getStatus()
            });
        }

    } 
    else if (searchType.equals("Specialization")) {//searching by specialization
        for (Teacher teacher : manager.searchTeacherBySpecialization(keyword)) {
            model.addRow(new Object[]{
                teacher.getTeacherId(),
                teacher.getTeacherName(),
                teacher.getDepartment(),
                teacher.getStatus()
            });
        }

    } 
    else if (searchType.equals("Status")) {//searching by status
        TeacherStatus status =TeacherStatus.valueOf(keyword.toUpperCase());

        for (Teacher teacher : manager.searchTeacherByStatus(status)) {
            model.addRow(new Object[]{
                teacher.getTeacherId(),
                teacher.getTeacherName(),
                teacher.getDepartment(),
                teacher.getStatus()
            });
        }
    }

    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No teachers found.");
    }

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this,"Teacher ID must be a number.");

} catch (IllegalArgumentException e) {
    JOptionPane.showMessageDialog(this,"Invalid status. Enter AVAILABLE, BUSY, ON_LEAVE, or INACTIVE.");
}
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStatusActionPerformed
    try {
    int teacherId = Integer.parseInt(jTextField2.getText());

    TeacherStatus status = TeacherStatus.valueOf(jComboBox2.getSelectedItem().toString());

    boolean updated = manager.updateTeacherStatus(teacherId, status);

    if (updated) {
        JOptionPane.showMessageDialog(this,"Teacher status updated successfully.");
        refreshTable();
    } 
    else {
        JOptionPane.showMessageDialog(this,"Teacher ID not found.");
    }

    } catch (NumberFormatException e) {
       JOptionPane.showMessageDialog(this,"Teacher ID must be a number.");
}
    }//GEN-LAST:event_btnUpdateStatusActionPerformed

