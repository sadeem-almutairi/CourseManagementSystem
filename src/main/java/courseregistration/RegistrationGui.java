private void loadTable() {

    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTable1.getModel();

    model.setRowCount(0);

    for (Registration r : Helpers.registrationList) {

        model.addRow(new Object[]{
            r.getRegistrationId(),
            r.getStudentId(),
            r.getStudentName(),
            r.getCourseId(),
            r.getCourseCode(),
            r.getSemester(),
            r.getRegistrationDate(),
            r.getRegistrationStatus()
        });

    }

}
private void loadStudents() {

    students = studentmanagement.Helpers.readFile();
System.out.println("Students = " + students.size());
    StudentIDbox.removeAllItems();
    StudentNamebox.removeAllItems();

    for (Student s : students) {

        StudentIDbox.addItem(s.getStudentID());
        StudentNamebox.addItem(s.getStudentName());

    }
}
private void loadCourses() {

    coursemanagement.Helpers.readFile();

    courses = coursemanagement.Helpers.courses;

    CourseIDbox.removeAllItems();
    CourseCodeBox.removeAllItems();

    for (Course c : courses) {

        CourseIDbox.addItem(String.valueOf(c.getCourseId()));
        CourseCodeBox.addItem(c.getCourseCode());

    }
}
