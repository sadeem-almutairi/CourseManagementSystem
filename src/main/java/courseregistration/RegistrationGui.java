    private void AddbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbtnActionPerformed
        try {

   Registration registration = new Registration(
        RegistrationIDtxt.getText(),
        StudentIDbox.getSelectedItem().toString(),
        StudentNamebox.getSelectedItem().toString(),
        Integer.parseInt(CourseIDbox.getSelectedItem().toString()),
        CourseCodeBox.getSelectedItem().toString(),
        SemesterBox.getSelectedItem().toString(),
        RegistrationDatetxt.getText(),
        RegistrationStatusbox.getSelectedItem().toString());
   Course c = courses.get(CourseIDbox.getSelectedIndex());

if (c.getStatus() != CourseStatus.OFFERED) {
    JOptionPane.showMessageDialog(this,
            "This course is not offered.");
    return;
}
int registered = manager.countRegistrationsByCourse(c.getCourseId());

if (registered >= c.getMaxSeats()) {
    JOptionPane.showMessageDialog(this,
            "No available seats.");
    return;
}

    if (manager.addRegistration(registration)) {

        Helpers.registrationList.add(registration);
        Helpers.writeFile();
        loadTable();

        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) jTable1.getModel();

        model.addRow(new Object[]{
            registration.getRegistrationId(),
            registration.getStudentId(),
            registration.getStudentName(),
            registration.getCourseId(),
            registration.getCourseCode(),
            registration.getSemester(),
            registration.getRegistrationDate(),
            registration.getRegistrationStatus()
        });

        javax.swing.JOptionPane.showMessageDialog(this,
                "Registration Added Successfully");
int courseId = Integer.parseInt(CourseIDbox.getSelectedItem().toString());

Counttxt.setText(String.valueOf(
        manager.countRegistrationsByCourse(courseId)
        
));
    } else {

        javax.swing.JOptionPane.showMessageDialog(this,
                "This Student already registered in this course");

    }

} catch (Exception e) {

    javax.swing.JOptionPane.showMessageDialog(this,
            "Please enter valid data");

}
    }//GEN-LAST:event_AddbtnActionPerformed

    private void SearchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchbtnActionPerformed
      String id = RegistrationIDtxt.getText();

Registration registration;

if (!id.trim().isEmpty()) {
    registration = manager.searchRegistration(id);
} else {
    registration = manager.searchByStudentId(
            StudentIDbox.getSelectedItem().toString());
}
if (registration != null) {

    StudentIDbox.setSelectedItem(registration.getStudentId());
StudentNamebox.setSelectedItem(registration.getStudentName());
CourseIDbox.setSelectedItem(String.valueOf(registration.getCourseId()));
CourseCodeBox.setSelectedItem(registration.getCourseCode());
SemesterBox.setSelectedItem(registration.getSemester());
RegistrationDatetxt.setText(registration.getRegistrationDate());
RegistrationStatusbox.setSelectedItem(registration.getRegistrationStatus());

    javax.swing.JOptionPane.showMessageDialog(this, "Registration Found");

} else {

    javax.swing.JOptionPane.showMessageDialog(this, "Registration Not Found");

}
    }//GEN-LAST:event_SearchbtnActionPerformed

