    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed

    int total = studentManagement.getAllStudents().size();

    int active = 0;
    int graduated = 0;
    int suspended = 0;
    int inactive = 0;

    int bscs = 0;
    int bsse = 0;
    int bba = 0;
    int mba = 0;

    int semester1 = 0;
    int semester2 = 0;
    int semester3 = 0;
    int semester4 = 0;
    int semester5 = 0;
    int semester6 = 0;
    int semester7 = 0;
    int semester8 = 0;

    for (Student student : studentManagement.getAllStudents()) {

        // Count Student Status
        switch (student.getStatus()) {

            case ACTIVE:
                active++;
                break;

            case GRADUATED:
                graduated++;
                break;

            case SUSPENDED:
                suspended++;
                break;

            case INACTIVE:
                inactive++;
                break;
        }

        // Count Programs
        switch (student.getProgram().toUpperCase()) {

            case "BSCS":
                bscs++;
                break;

            case "BSSE":
                bsse++;
                break;

            case "BBA":
                bba++;
                break;

            case "MBA":
                mba++;
                break;
        }

        // Count Semesters
        switch (student.getSemester()) {

            case "1":
                semester1++;
                break;

            case "2":
                semester2++;
                break;

            case "3":
                semester3++;
                break;

            case "4":
                semester4++;
                break;

            case "5":
                semester5++;
                break;

            case "6":
                semester6++;
                break;

            case "7":
                semester7++;
                break;

            case "8":
                semester8++;
                break;
        }
    }

    String report =
            "========== STUDENT REPORT ==========\n\n"

            + "Total Students : " + total + "\n\n"

            + "Student Status\n"
            + "----------------------------\n"
            + "Active      : " + active + "\n"
            + "Graduated : " + graduated + "\n"
            + "Suspended : " + suspended + "\n"
            + "Inactive    : " + inactive + "\n\n"

            + "Programs\n"
            + "----------------------------\n"
            + "BSCS : " + bscs + "\n"
            + "BSSE : " + bsse + "\n"
            + "BBA  : " + bba + "\n"
            + "MBA  : " + mba + "\n\n"

            + "Semesters\n"
            + "----------------------------\n"
            + "Semester 1 : " + semester1 + "\n"
            + "Semester 2 : " + semester2 + "\n"
            + "Semester 3 : " + semester3 + "\n"
            + "Semester 4 : " + semester4 + "\n"
            + "Semester 5 : " + semester5 + "\n"
            + "Semester 6 : " + semester6 + "\n"
            + "Semester 7 : " + semester7 + "\n"
            + "Semester 8 : " + semester8;

    JOptionPane.showMessageDialog(
            this,
            report,
            "Student Report",
            JOptionPane.INFORMATION_MESSAGE
    );


    }//GEN-LAST:event_btnReportActionPerformed

