    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String studentID = txtStudentID.getText().trim();
    String studentName = txtStudentName.getText().trim();
    String program = cmbProgram.getSelectedItem().toString();
    String semester = cmbSemester.getSelectedItem().toString();
    String phone = txtPhone.getText().trim();
    String email = txtEmail.getText().trim();
    String cgpaText = txtCGPA.getText().trim();
    String statusText = cmbStatus.getSelectedItem().toString();

    // Check empty fields
    if (studentID.isEmpty() || studentName.isEmpty()
            || phone.isEmpty() || email.isEmpty() || cgpaText.isEmpty()) {

        JOptionPane.showMessageDialog(this,
                "Please fill in all required fields.");
        return;
    }

    // Phone validation (8 digits)
    if (!phone.matches("\\d{8}")) {
        JOptionPane.showMessageDialog(this,
                "Phone number must contain exactly 8 digits.");
        return;
    }

    // Email validation
    if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        JOptionPane.showMessageDialog(this,
                "Invalid email address.");
        return;
    }

    // CGPA validation
    double cgpa;

    try {

        cgpa = Double.parseDouble(cgpaText);

        if (cgpa < 0 || cgpa > 4) {
            JOptionPane.showMessageDialog(this,
                    "CGPA must be between 0.0 and 4.0");
            return;
        }

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(this,
                "CGPA must be a number.");
        return;

    }

    // Convert status
    StudentStatus status = StudentStatus.valueOf(statusText);

    // Create Student object
    Student student = new Student(
            studentID,
            studentName,
            program,
            semester,
            phone,
            email,
            cgpa,
            status
    );

    // Add student
    boolean added = studentManagement.addStudent(student);

    if (!added) {
        JOptionPane.showMessageDialog(this,
                "Student ID already exists.");
        return;
    }

    refreshTable();

    JOptionPane.showMessageDialog(this,
            "Student added successfully.");

    // Clear fields
    txtStudentID.setText("");
    txtStudentName.setText("");
    txtPhone.setText("");
    txtEmail.setText("");
    txtCGPA.setText("");

    cmbProgram.setSelectedIndex(0);
    cmbSemester.setSelectedIndex(0);
    cmbStatus.setSelectedIndex(0);

    txtStudentID.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      String studentID = txtStudentID.getText().trim();

if (studentID.isEmpty()) {
    JOptionPane.showMessageDialog(this,
            "Please enter Student ID.");
    return;
}

int choice = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete this student?",
        "Confirm Delete",
        JOptionPane.YES_NO_OPTION
);

if (choice == JOptionPane.YES_OPTION) {

    boolean deleted = studentManagement.deleteStudent(studentID);

    if (deleted) {

        refreshTable();

        txtStudentID.setText("");
        txtStudentName.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtCGPA.setText("");

        cmbProgram.setSelectedIndex(0);
        cmbSemester.setSelectedIndex(0);
        cmbStatus.setSelectedIndex(0);

        JOptionPane.showMessageDialog(this,
                "Student deleted successfully.");

    } else {

        JOptionPane.showMessageDialog(this,
                "Student not found.");

    }

  }
    }//GEN-LAST:event_btnDeleteActionPerformed

