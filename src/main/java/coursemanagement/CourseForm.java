    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      if (txtCourseID.getText().trim().isEmpty()
        || txtCourseCode.getText().trim().isEmpty()
        || txtCourseTitle.getText().trim().isEmpty()
        || txtCreditHours.getText().trim().isEmpty()
        || txtDepartment.getText().trim().isEmpty()
        || txtMaxSeats.getText().trim().isEmpty()
        || txtLevel.getText().trim().isEmpty()) {

    JOptionPane.showMessageDialog(
            this,
            "Please fill in all fields."
    );

    return;
}
        
        try {

    int id = Integer.parseInt(txtCourseID.getText());
    if (manager.courseIdExists(id)) {

    JOptionPane.showMessageDialog(
            this,
            "Course ID already exists."
    );

    return;
}
    String code = txtCourseCode.getText();
    String title = txtCourseTitle.getText();
    int creditHours = Integer.parseInt(txtCreditHours.getText());
    String department = txtDepartment.getText();
    int maxSeats = Integer.parseInt(txtMaxSeats.getText());
    int level = Integer.parseInt(txtLevel.getText());
    if (id <= 0 || creditHours <= 0 || maxSeats <= 0 || level <= 0) {

    JOptionPane.showMessageDialog(
            this,
            "Course ID, credit hours, maximum seats, and level must be greater than zero."
    );

    return;
}
   // Converts the selected ComboBox item into a CourseStatus enum
CourseStatus status = CourseStatus.fromString(
        cmbStatus.getSelectedItem().toString());
    
    Course course = new Course(id, code, title, creditHours, department, maxSeats, level, status);
    manager.addCourse(course);
    Helpers.writeFile();
    DefaultTableModel model =(DefaultTableModel) tblCourses.getModel();

    model.addRow(new Object[]{
        course.getCourseId(),course.getCourseCode(),course.getCourseTitle(),
        course.getCreditHours(),course.getDepartment(),course.getMaxSeats(),
        course.getLevel(),course.getStatus()});

    
    JOptionPane.showMessageDialog(
            this,"Course added successfully!"
    );

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(
            this,"Please enter valid numbers." );
        }
 
 
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         int selectedRow = tblCourses.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(
                this,
                "Please select a course from the table."
        );
        return;
    }

    int courseId = Integer.parseInt(
            tblCourses.getValueAt(selectedRow, 0).toString()
    );

    boolean deleted = manager.deleteCourse(courseId);

    if (deleted) {
        Helpers.writeFile();

        DefaultTableModel model =
                (DefaultTableModel) tblCourses.getModel();

        model.removeRow(selectedRow);

        JOptionPane.showMessageDialog(
                this,
                "Course deleted successfully!"
        );
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String keyword = JOptionPane.showInputDialog(
        this,"Enter course ID, code, title, department, or status:");

if (keyword != null && !keyword.trim().isEmpty()) {

    java.util.ArrayList<Course> results = manager.searchCourses(keyword.trim());

    DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
    model.setRowCount(0);
    
    for (Course course : results) {
        model.addRow(new Object[]{
            course.getCourseId(),course.getCourseCode(), course.getCourseTitle(),
            course.getCreditHours(),course.getDepartment(),course.getMaxSeats(),
            course.getLevel(),course.getStatus()});
    }
    if (results.isEmpty()) {
        JOptionPane.showMessageDialog(
                this,"No matching course found.");
    }
}
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
    int totalCourses = manager.getAllCourses().size();
    int offeredCourses = 0;
    int notOfferedCourses = 0;
    int fullCourses = 0;

    for (Course course : manager.getAllCourses()) {
        // Counts courses according to their enum status
     if (course.getStatus() == CourseStatus.OFFERED) {
     offeredCourses++;
  }
     else if (course.getStatus() == CourseStatus.NOT_OFFERED) {
     notOfferedCourses++;
  }
     else if (course.getStatus() == CourseStatus.FULL) {
     fullCourses++;
}
    }

    String departmentReport = "";

    java.util.ArrayList<String> countedDepartments = new java.util.ArrayList<>();

    for (Course course : manager.getAllCourses()) {
        String department = course.getDepartment();

        if (!countedDepartments.contains(department.toLowerCase())) {
            int departmentCount = 0;

            for (Course otherCourse : manager.getAllCourses()) {

                if (otherCourse.getDepartment().equalsIgnoreCase(department)) {
                    departmentCount++;
                }
            }

            departmentReport +="\n" + department + ": " + departmentCount;
            countedDepartments.add(department.toLowerCase());
        }
    }

    String report =
            "Total Courses: " + totalCourses
            + "\nOffered Courses: " + offeredCourses
            + "\nNot Offered Courses: " + notOfferedCourses
            + "\nFull Courses: " + fullCourses
            + "\n\nCourses by Department:"
            + departmentReport;

    JOptionPane.showMessageDialog(
            this,
            report,
            "Course Report",
            JOptionPane.INFORMATION_MESSAGE
    );


    }//GEN-LAST:event_btnReportActionPerformed

