    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblCourses.getSelectedRow();
        int originalCourseId = Integer.parseInt(
        tblCourses.getValueAt(selectedRow, 0).toString()
);
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(
            this,
            "Please select a course from the table."
    );
} else {
    try {
        int id = Integer.parseInt(txtCourseID.getText());
        String code = txtCourseCode.getText();
        String title = txtCourseTitle.getText();
        int creditHours = Integer.parseInt(txtCreditHours.getText());
        String department = txtDepartment.getText();
        int maxSeats = Integer.parseInt(txtMaxSeats.getText());
        int level = Integer.parseInt(txtLevel.getText());
        // Converts the selected ComboBox item into a CourseStatus enum
        CourseStatus status = CourseStatus.fromString(
        cmbStatus.getSelectedItem().toString());
        // Creates the updated Course object
        Course updatedCourse = new Course(id,code,title,creditHours,department, 
                maxSeats,level,status);

        manager.updateCourse(originalCourseId, updatedCourse);
        Helpers.writeFile();

        DefaultTableModel model =
                (DefaultTableModel) tblCourses.getModel();

        model.setValueAt(id, selectedRow, 0);
        model.setValueAt(code, selectedRow, 1);
        model.setValueAt(title, selectedRow, 2);
        model.setValueAt(creditHours, selectedRow, 3);
        model.setValueAt(department, selectedRow, 4);
        model.setValueAt(maxSeats, selectedRow, 5);
        model.setValueAt(level, selectedRow, 6);
        model.setValueAt(status, selectedRow, 7);

        JOptionPane.showMessageDialog(
                this,
                "Course updated successfully!"
        );

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(
                this,
                "Please enter valid numbers."
        );
    }
}
    }//GEN-LAST:event_btnUpdateActionPerformed

